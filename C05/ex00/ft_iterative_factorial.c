int	ft_iterative_factorial(int nb)
{
	int a;

	if (nb < 0 || nb >= 13)
		return (0);
	if (nb == 0)
		return (1);
	a = nb;
	while (nb > 1)
	{
		a = a * (nb - 1);
		nb--;
	}
	return (a);
}
